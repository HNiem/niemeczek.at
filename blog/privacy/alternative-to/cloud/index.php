							<h1>Cloud-Alternativen</h1>
							<p><br /><br />
							Kostenlose, öffentliche Cloud-Dienste wie beispielsweise Google Drive, Dropbox oder OneDrive sind absolut nicht zu empfehlen.
							Schließlich verdienen diese an der Analyse unserer Daten, und Schutz vor der Weitergabe an Geheimdienste bieten diese auch nicht. 
							Auch Edward Snowden <a target="_blank" href="http://derstandard.at/2000003294538/Edward-Snowden-Dropbox-ist-extrem-gefaehrlich">warnte bereits mehrfach</a> 
							vor der Nutzung von Dropbox.
							<br /><br />
							Günstige Alternativen dazu gibt es: 
							<br /><br />
							</p>
							<h3>SPIDEROAK ONE</h3>
							<p>
							Der von Snowden empfohlene Cloud-Dienst <a target="_blank" href="https://spideroak.com/solutions/spideroak-one">Spideroak</a> bietet für 5$ monatlich 100GB, 
							für 9$ 250GB und für 12$ 1000GB (1 Terrabyte, etwa so viel wie eine Festplatte speichern kann).
							Das Unternehmen gibt an, alle Daten vor dem Upload zu verschlüsseln und so guten Schutz der Privatspäre zu bieten.
							<br /><br />
							</p>
							
							<h3>Owncloud</h3>
							<p>
								<a target="_blank" href="https://owncloud.org/">Owncloud</a> ist eine Open Source Cloud-Software, die mit Android-
								(<a target="_blank" href="https://play.google.com/store/apps/details?id=com.owncloud.android">PlayStore</a> und 
								<a target="_blank" href="https://f-droid.org/repository/browse/?fdfilter=owncloud&fdid=com.owncloud.android">F-Droid</a>)
								<a target="_blank" href="https://itunes.apple.com/de/app/owncloud/id543672169?mt=8">IOS</a>-Apps auch für Mobilgeräte tauglich ist.
								Natürlich gibt es auch <a target="_blank" href="https://owncloud.com/products/desktop-clients/">Desktop-Anwendungen</a> für Windows, Mac und Linux.
								<br />
								<br />
								Die Software bietet zusätzlich auch
								synchronisierbare <a target="_blank" href="/blog/privacy/alternative-to/kalender/">Kalender und Aufgabenverwaltung</a>,
								synchronisierbare <a target="_blank" href="/blog/privacy/alternative-to/kontaktverwaltung/">Kontaktverwaltung</a> und viele andere, durch Plugins ergänzbare Funktionen.
								<br />
								<br />
								Zum einen gibt es Anbieter, die Owncloud ähnlich wie Spideroak anbieten, dabei werden die Daten allerdings nicht auf dem Nutzerrechner verschlüsselt 
								(sondern erst am Server oder möglicherweise auch gar nicht). Die sind daher nicht zu empfehlen.
								<br />
								<br />
								Die andere Alternative ist, sich daheim selbst einen kleinen Server aufzusetzen. Es gibt schon um wenig Geld Server, die einen geringen Stromverbrauch 
								haben und auf denen man eine solche Cloud selbst daheim betreiben kann.
								<br /><br />
								<b>Der Vorteil:</b> Die meisten Zugriffe auf die Cloud finden vermutlich vom Rechner oder Smartphone zu Hause statt. So hat man dort eine sehr schnelle 
								Anbindung. Für den Abruf der Kalendertermine, 
								einzelner Dokumente oder Kontakte reicht dann auch die langsame Anbindung des Internetproviders.
								<br /><br />
								Auf meiner Seite <a target="_blank" href="https://debianadmin.net/">Debianadmin.net</a> zeige ich, 
								wie man einen Server unter Debian sicher einrichten kann und wie die Owncloud aufgesetzt werden kann.
								
								
